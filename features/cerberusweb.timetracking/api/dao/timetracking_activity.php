	function getContext($timetracking_activity, &$token_labels, &$token_values, $prefix=null) {
		if(is_null($prefix))
			$prefix = 'TimeTracking Activity:';
		
		$translate = DevblocksPlatform::getTranslationService();
		$fields = DAO_CustomField::getByContext(CerberusContexts::CONTEXT_TIMETRACKING_ACTIVITY);

		// Polymorph
		if(is_numeric($timetracking_activity)) {
			$timetracking_activity = DAO_TimeTrackingActivity::get($timetracking_activity);
		} elseif($timetracking_activity instanceof Model_TimeTrackingActivity) {
			// It's what we want already.
		} elseif(is_array($timetracking_activity)) {
			$timetracking_activity = Cerb_ORMHelper::recastArrayToModel($timetracking_activity, 'Model_TimeTrackingActivity');
		} else {
			$timetracking_activity = null;
		}
		
		// Token labels
		$token_labels = array(
			'_label' => $prefix,
			'id' => $prefix.$translate->_('common.id'),
			'name' => $prefix.$translate->_('common.name'),
			'updated_at' => $prefix.$translate->_('common.updated'),
			'record_url' => $prefix.$translate->_('common.url.record'),
		);
		
		// Token types
		$token_types = array(
			'_label' => 'context_url',
			'id' => Model_CustomField::TYPE_NUMBER,
			'name' => Model_CustomField::TYPE_SINGLE_LINE,
			'updated_at' => Model_CustomField::TYPE_DATE,
			'record_url' => Model_CustomField::TYPE_URL,
		);
		
		// Custom field/fieldset token labels
		if(false !== ($custom_field_labels = $this->_getTokenLabelsFromCustomFields($fields, $prefix)) && is_array($custom_field_labels))
			$token_labels = array_merge($token_labels, $custom_field_labels);
		
		// Custom field/fieldset token types
		if(false !== ($custom_field_types = $this->_getTokenTypesFromCustomFields($fields, $prefix)) && is_array($custom_field_types))
			$token_types = array_merge($token_types, $custom_field_types);
		
		// Token values
		$token_values = [];
		
		$token_values['_context'] = CerberusContexts::CONTEXT_TIMETRACKING_ACTIVITY;
		$token_values['_types'] = $token_types;
		
		if($timetracking_activity) {
			$token_values['_loaded'] = true;
			$token_values['_label'] = $timetracking_activity->name;
			$token_values['id'] = $timetracking_activity->id;
			$token_values['name'] = $timetracking_activity->name;
			$token_values['updated_at'] = $timetracking_activity->updated_at;
			
			// Custom fields
			$token_values = $this->_importModelCustomFieldsAsValues($timetracking_activity, $token_values);
			
			// URL
			$url_writer = DevblocksPlatform::services()->url();
			$token_values['record_url'] = $url_writer->writeNoProxy(sprintf("c=profiles&type=timetracking_activity&id=%d-%s",$timetracking_activity->id, DevblocksPlatform::strToPermalink($timetracking_activity->name)), true);
		}
		
		return true;
	}
	
